from typing import List
class Solution:

    def removeC(self,grid) :
        for i in range(len(grid)) :
            grid[i] = grid[i][1:]
        return grid
    
    
    def rec(self,grid,r,c,d) :
        if (r,c) in d :
            return d[(r,c)]
        if r==1 and c == 1 and grid[0][0]==0:
            return 1
        
        else :
            
            if r == 0 or c == 0:
                return 0
            if grid[0][0] == 1 : 
                return 0
            
            else :
                d[(r,c)] = self.rec(grid[1:],r-1,c,d) + self.rec(self.removeC(grid),r,c-1,d)
                return d[(r,c)]
    
